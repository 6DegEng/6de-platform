class TestParseSheet:
    def test_parses_correct_row_count(self, legacy_xlsx, col_map):
        rows = parse_sheet(legacy_xlsx, sheet_name="Projects", column_map=col_map)
        assert len(rows) == 6

    def test_maps_columns_to_platform_fields(self, legacy_xlsx, col_map):
        rows = parse_sheet(legacy_xlsx, sheet_name="Projects", column_map=col_map)
        first = rows[0]
        assert "job_number" in first
        assert "name" in first
        assert "status" in first
        assert first["job_number"] == 260101 or str(first["job_number"]) == "260101"

    def test_unmapped_columns_excluded(self, legacy_xlsx, col_map):
        rows = parse_sheet(legacy_xlsx, sheet_name="Projects", column_map=col_map)
        first = rows[0]
        assert "Process No." not in first
        assert "Service Type" not in first

    def test_empty_sheet_returns_empty(self, tmp_path, col_map):
        import openpyxl
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "Projects"
        file_path = tmp_path / "empty.xlsx"
        wb.save(file_path)
        wb.close()
        rows = parse_sheet(file_path, sheet_name="Projects", column_map=col_map)
        assert rows == []


