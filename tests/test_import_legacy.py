class TestNormalizeRow:
    def test_status_mapped(self, col_map):
        raw = {"status": "AHJ/Permitting", "job_number": "260301", "name": "Test"}
        norm = normalize_row(raw, col_map)
        assert norm["status"] == "ahj_permitting"

    def test_priority_mapped(self, col_map):
        raw = {"priority": "Medium", "job_number": "260001", "name": "Test"}
        norm = normalize_row(raw, col_map)
        assert norm["priority"] == "normal"

    def test_priority_emoji_stripped(self, col_map):
        raw = {"priority": "🔴 High", "job_number": "260001", "name": "Test"}
        norm = normalize_row(raw, col_map)
        assert norm["priority"] == "high"

    def test_percent_decimal_converted(self, col_map):
        raw = {"percent_complete": 0.45, "job_number": "260001", "name": "Test"}
        norm = normalize_row(raw, col_map)
        assert norm["percent_complete"] == 45

    def test_percent_string_converted(self, col_map):
        raw = {"percent_complete": "75%", "job_number": "260001", "name": "Test"}
        norm = normalize_row(raw, col_map)
        assert norm["percent_complete"] == 75

    def test_job_number_padded(self, col_map):
        raw = {"job_number": "1234", "name": "Test"}
        norm = normalize_row(raw, col_map)
        assert norm["job_number"] == "001234"

    def test_default_status_active(self, col_map):
        raw = {"job_number": "260001", "name": "Test"}
        norm = normalize_row(raw, col_map)
        assert norm["status"] == "active"


