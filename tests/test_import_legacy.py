class TestClassifyRow:
    def test_new_row_creates(self):
        row = {"job_number": "260101", "name": "Test", "status": "active"}
        outcome, errors = classify_row(row, 0, existing_jobs=set())
        assert outcome == Outcome.CREATE
        assert errors == []

    def test_existing_job_updates(self):
        row = {"job_number": "260101", "name": "Test", "status": "active"}
        outcome, errors = classify_row(row, 0, existing_jobs={"260101"})
        assert outcome == Outcome.UPDATE
        assert errors == []

    def test_invalid_row_fails(self):
        row = {"job_number": "260101", "name": "Test", "status": "bogus"}
        outcome, errors = classify_row(row, 0, existing_jobs=set())
        assert outcome == Outcome.FAIL
        assert len(errors) > 0


