class TestJobCodeGenerator:
    def test_first_code_for_prefix(self, db):
        code = next_job_code(db, prefix="SD", year=2026)
        assert code == "26SD01"

    def test_sequential_codes(self, db):
        db.execute(
            "INSERT INTO projects (job_number, name) VALUES ('26SD01', 'First')"
        )
        db.commit()
        code = next_job_code(db, prefix="SD", year=2026)
        assert code == "26SD02"

    def test_different_prefix_resets(self, db):
        db.execute(
            "INSERT INTO projects (job_number, name) VALUES ('26SD05', 'Existing')"
        )
        db.commit()
        code = next_job_code(db, prefix="RO", year=2026)
        assert code == "26RO01"

    def test_default_year_is_current(self, db):
        from datetime import date
        yy = str(date.today().year)[-2:]
        code = next_job_code(db, prefix="SD")
        assert code.startswith(yy)


# ---------------------------------------------------------------------------
# Dry-run smoke test — full pipeline on synthetic fixture
# ---------------------------------------------------------------------------
class TestDryRunSmoke:
    def test_full_pipeline_produces_report(self, legacy_xlsx, col_map, tmp_path):
        """Run the full parse → normalize → classify pipeline on the fixture."""
        rows = parse_sheet(legacy_xlsx, sheet_name="Projects", column_map=col_map)
        assert len(rows) == 6

        normalized = [normalize_row(r, col_map) for r in rows]
        existing_jobs: set[str] = set()

        results = []
        for i, row in enumerate(normalized):
            outcome, errors = classify_row(row, i, existing_jobs)
            results.append({
                "row": i + 1,
                "job_number": row.get("job_number"),
                "name": row.get("name"),
                "outcome": outcome,
                "errors": errors,
            })

        report = build_report(results, str(legacy_xlsx), "Projects", commit=False)

        # Verify report shape
        s = report["summary"]
        assert s["mode"] == "DRY-RUN"
        assert s["total_rows"] == 6
        assert s["create"] >= 3  # at least the valid rows
        assert s["fail"] >= 1    # at least the invalid-status row

        # Verify the report can be serialized to JSON
        report_path = tmp_path / "report.json"
        with open(report_path, "w") as f:
            json.dump(report, f, default=str)
        with open(report_path) as f:
            reloaded = json.load(f)
        assert reloaded["summary"]["total_rows"] == 6
