class TestReportShape:
    def test_report_has_required_keys(self):
        results = [
            {"row": 1, "job_number": "260101", "name": "A", "outcome": Outcome.CREATE, "errors": []},
            {"row": 2, "job_number": "260201", "name": "B", "outcome": Outcome.UPDATE, "errors": []},
            {"row": 3, "job_number": "260301", "name": "C", "outcome": Outcome.FAIL,
             "errors": ["Invalid status"]},
        ]
        report = build_report(results, "test.xlsx", "Projects", commit=False)

        assert "summary" in report
        assert "rows" in report
        s = report["summary"]
        assert s["file"] == "test.xlsx"
        assert s["sheet"] == "Projects"
        assert s["mode"] == "DRY-RUN"
        assert s["total_rows"] == 3
        assert s["create"] == 1
        assert s["update"] == 1
        assert s["fail"] == 1

    def test_commit_mode_in_report(self):
        report = build_report([], "test.xlsx", None, commit=True)
        assert report["summary"]["mode"] == "COMMIT"


